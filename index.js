// We use comparison operators to compare the value of a variable with something else.

let x = 1;

// The result of an expression that includes a comparison operator is either true or false.

// Relational operators
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

// Equality operators
console.log(x === 1);
console.log(x !== 1);
