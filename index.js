// Before ES6, we used the 'var' keyword to define variables, but it had some issues. Now, we prefer using 'let' and 'const' keywords.

let name;
console.log(name); // undefined - Variables are initially assigned the value 'undefined' by default when declared.

let name2 = "Zahra";
console.log(name2);

// Rules for naming variables:
// - Cannot be a reserved keyword like if, let, else, where, ...
// - Should have meaningful and descriptive names (avoid names like x, a, b, ...)
// - Cannot start with a number, e.g., 1name
// - Cannot contain a space or hyphen (-)
// - Should follow camel notation, e.g., firstName
// - Are case-sensitive
// - Each variable should be defined on a single line.
